		/// To arbitrarily rotate and flip icons, use the fas-rotate-* and fas-flip-* classes. 
		/// </summary>
		public const string FlipHorizontal = "fas-flip-horizontal";

		/// <summary>
		/// To arbitrarily rotate and flip icons, use the fas-rotate-* and fas-flip-* classes. 
		/// </summary>
		public const string FlipVertical = "fas-flip-vertical";

		/// <summary>
		/// To stack multiple icons, use the fas-stack class on the parent, the fas-stack-1x 
		/// for the regularly sized icon, and fas-stack-2x for the larger icon. fas-inverse 
		/// can be used as an alternative icon color. You can even throw larger icon classes 
		/// on the parent to get further control of sizing. 
		/// </summary>
		public const string Stack = "fas-stack";

