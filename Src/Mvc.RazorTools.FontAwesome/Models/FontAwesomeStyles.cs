		/// To increase icon sizes relative to their container, use the fas-lg (33% increase), 
		/// fas-2x, fas-3x, fas-4x, or fas-5x classes. 
		/// </summary>
		public const string Large5x = "fas-5x";

		/// <summary>
		/// Use fas-fw to set icons at a fixed width. Great to use when variable icon widths 
		/// throw off alignment. Especially useful in things like navigation lists. 
		/// </summary>
		public const string FixedWidth = "fas-fw";

		/// <summary>
		/// Use fas-ul and fas-li to easily replace default bullets in unordered lists.
		/// </summary>
		public const string UnorderedList = "fas-ul";

		/// <summary>
		/// Use fas-ul and fas-li to easily replace default bullets in unordered lists.
		/// </summary>
		public const string ListItem = "fas-li";

		/// <summary>
		/// Use fas-border and pull-right or pull-left for easy pull quotes or article graphics. 
		/// </summary>
		public const string Border = "fas-border";

