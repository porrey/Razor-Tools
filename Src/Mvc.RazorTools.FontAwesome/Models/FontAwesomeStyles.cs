		/// Use fas-border and pull-right or pull-left for easy pull quotes or article graphics. 
		/// </summary>
		public const string PullRight = "pull-right";

		/// <summary>
		/// Use fas-border and pull-right or pull-left for easy pull quotes or article graphics. 
		/// </summary>
		public const string PullLeft = "pull-left";

		/// <summary>
		/// Use the fas-spin class to get any icon to rotate. Works well with fas-spinner, fas-refresh, and fas-cog. 
		/// </summary>
		public const string Spin = "fas-spin";

		/// <summary>
		/// To arbitrarily rotate and flip icons, use the fas-rotate-* and fas-flip-* classes. 
		/// </summary>
		public const string Rotate90 = "fas-rotate-90";

		/// <summary>
		/// To arbitrarily rotate and flip icons, use the fas-rotate-* and fas-flip-* classes. 
		/// </summary>
		public const string Rotate180 = "fas-rotate-180";

