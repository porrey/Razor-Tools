		/// Defines the i tag as a Font Awesome icon
		/// </summary>
		public const string Icon = "fas";

		/// <summary>
		/// To increase icon sizes relative to their container, use the fas-lg (33% increase), 
		/// fas-2x, fas-3x, fas-4x, or fas-5x classes. 
		/// </summary>
		public const string Large1x = "fas-lg";

		/// <summary>
		/// To increase icon sizes relative to their container, use the fas-lg (33% increase), 
		/// fas-2x, fas-3x, fas-4x, or fas-5x classes. 
